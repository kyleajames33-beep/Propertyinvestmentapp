// Get today's property based on the date (cycles through the list)
export function getTodaysProperty(): DailyProperty {
  const today = new Date();
  const start = new Date('2024-01-01');
  const diff = Math.floor((today.getTime() - start.getTime()) / (1000 * 60 * 60 * 24));
  const index = diff % dailyProperties.length;
  return dailyProperties[index];
}

// Get property for a specific date string (YYYY-MM-DD)
export function getPropertyForDate(dateStr: string): DailyProperty {
  const date = new Date(dateStr);
  const start = new Date('2024-01-01');
  const diff = Math.floor((date.getTime() - start.getTime()) / (1000 * 60 * 60 * 24));
  const index = Math.abs(diff) % dailyProperties.length;
  return dailyProperties[index];
}

// Score a guess (0-100)
// Within 1% = 100pts, within 5% = 80pts, within 10% = 60pts, within 20% = 40pts, within 50% = 20pts, else 0
export function scoreGuess(guess: number, actual: number): number {
  const diff = Math.abs(guess - actual);
  const pct = diff / actual;
  if (pct <= 0.01) return 100;
  if (pct <= 0.05) return 80;
  if (pct <= 0.10) return 60;
  if (pct <= 0.20) return 40;
  if (pct <= 0.50) return 20;
  return 0;
}

// Get accuracy label
export function getAccuracyLabel(score: number): string {
  if (score === 100) return 'Bullseye!';
  if (score >= 80) return 'Excellent';
  if (score >= 60) return 'Good guess';
  if (score >= 40) return 'Not bad';
  if (score >= 20) return 'Keep practicing';
  return 'Way off';
}

