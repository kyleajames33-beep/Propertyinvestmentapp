export function getAccuracyEmoji(score: number): string {
  if (score === 100) return '🎯';
  if (score >= 80) return '🔥';
  if (score >= 60) return '👍';
  if (score >= 40) return '🤔';
  if (score >= 20) return '😬';
  return '😵';
}

// Format price for display
export function formatPrice(price: number): string {
  if (price >= 1000000) {
    return `$${(price / 1000000).toFixed(2)}m`;
  }
  return `$${(price / 1000).toFixed(0)}k`;
}

// Format full price
export function formatFullPrice(price: number): string {
  return new Intl.NumberFormat('en-AU', { style: 'currency', currency: 'AUD', maximumFractionDigits: 0 }).format(price);
}
